import random


def count_sort(ls, max_count=10):
    count = [0 for c in range(max_count+1)]
    for var in ls:
        count[var] += 1
    ls.clear()
    for ind, val in enumerate(count):
        for i in range(val):
            ls.append(ind)

    print(ls)

ls = [random.randint(0, 10) for i in range(10)]


print(ls)
count_sort(ls)
